# List - changeable. Allows duplicate members.
Product = ['Mobile', 'Headset', 'Speaker']
Product[0] = 'Playstation'
print(Product)

# Tuple - unchangeable. Allows duplicate members.
Company = ('Sony', 'Marshal', 'Bose', 'Sony')
print(Company)

# Dictionaries - changeable. No duplicate members.
Dict = {"Harry": 21, "Marry": 22}
Dict["Harry"]= 39
print(Dict)

# Sets - unordered, unchangeable, and unindexed. No duplicate members.
thesets = {"apple", "pineapple", True, 1, 2}
print(thesets)

